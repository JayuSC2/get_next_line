#include "get_next_line.h"

char	*ft_strdup(const char *s)
{
	char	*new;
	int		i;
	int		len;

	len = 0;
	while (s[len] != '\0')
		len++;
	new = malloc(sizeof(char) * (len + 1));
	if (!new)
		return (NULL);
	i = 0;
	while (i < len)
	{
		new[i] = s[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}